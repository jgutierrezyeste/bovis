<h3>¿Quién puede formar parte de la Red?</h3>

<div>
<p style="text-align:justify;">Podrán formar parte del <strong>Directorio</strong>:</p>

<ul>
	<li style="text-align:justify;">Propietarios o gestores, empresas de transformación y de servicios, asociaciones, investigadores y otros actores relacionados directa o indirectamente con la dehesa, con interés en recibir información y conocimiento que contribuya a la valorización económica y ambiental de este sistema.</li>
</ul>

<p style="text-align:justify;">Además del Directorio, del que formarán parte todos los miembros de la Red, se invitará a formar parte del <strong>Mapa de la Red</strong> a aquellos miembros que reúnan las siguientes características:</p>

<ul>
	<li style="text-align:justify;">Propietarios o gestores de dehesa y empresas de transformación y de servicios que implementen buenas prácticas o innovaciones a través de las cuáles apoyen la valorización y sostenibilidad de estos sistemas.</li>
	<li style="text-align:justify;">Entidades públicas (universidades, administración) o privadas (ONGs, etc.), propietarias o gestoras de dehesas, comprometidas con la experimentación y demostración de buenas prácticas.</li>
	<li style="text-align:justify;">Otras entidades comprometidas con el intercambio y la transferencia de conocimiento y de experiencias para la valorización y sostenibilidad de la dehesa.</li>
</ul>
</div>

<h3>¿En qué consiste la participación?</h3>

<div>
<p style="text-align:justify;">La inclusión en el <strong>Directorio</strong> implica:</p>

<ul>
	<li style="text-align:justify;">Disponibilidad para ser contactado por otros miembros de la Red.</li>
	<li style="text-align:justify;">Facilitar la siguiente información: nombre de la persona/entidad, áreas de investigación, experiencia o trabajo, proyectos relacionados con la dehesa en los que participa, localización, contacto.</li>
</ul>

<p style="text-align:justify;">La inclusión en el <strong>Mapa de la Red </strong>requiere:</p>

<ul>
	<li style="text-align:justify;">Publicar un breve perfil en el Mapa, indicando (según el perfil de la entidad participante): localización, tipos de aprovechamiento, innovaciones y buenas prácticas aplicadas (fincas), líneas de investigación o trabajo (centros de I+D, asociaciones, etc.), productos o servicios que proveen que contribuyan, a través de la innovación, a la valorización y sostenibilidad de la dehesa (empresas de transformación/servicios).</li>
</ul>
</div>

